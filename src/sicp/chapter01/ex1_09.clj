(defn +
  [a b]
  (if (zero? a)
    b
    (+ (dec a) (inc b))))

; (+ 4 5)
; (+ 3 6)
; (+ 2 7)
; (+ 1 8)
; (+ 0 9)
; 9
;отложенных операций в данном процессе нет: процесс итеративный
